-- day, month, year
SELECT 
now(), 
day(now())as days,
month(now()) as months, 
year(now())  as years;


-- sysdate & now
SELECT SYSDATE(), SLEEP(5), SYSDATE();
SELECT NOW(), SLEEP(5), NOW();  

-- get  current date & time
SELECT 
now(),
sysdate(),
CURRENT_TIMESTAMP(),
CURRENT_DATE(),
CURRENT_TIME()
;

-- DATE
SELECT DATE('2008-01-02 01:02:03');
SELECT DATE('20080102');
SELECT DATE('2008/01/02');

-- DATEDIFF
SELECT DATEDIFF("2017-06-25", "2017-06-15");
SELECT DATEDIFF("2017-01-01", "2016-12-24");
SELECT DATEDIFF("2017-06-25 09:34:21", "2017-06-15 15:25:35");


-- DATE_ADD & ADDDATE
SELECT DATE_ADD('2008-01-02', INTERVAL 31 DAY);
SELECT DATE_ADD('2008-01-02', INTERVAL -31 DAY);

SELECT ADDDATE('2008-01-02', INTERVAL 31 DAY);
SELECT ADDDATE('2008-01-02', 31);
SELECT ADDDATE('2008-01-02', INTERVAL 2 MONTH);
SELECT ADDDATE('2008-01-02', INTERVAL 10 YEAR);




-- ADDTIME
SELECT ADDTIME('2007-12-31 23:59:59', '1:1:1');
SELECT ADDTIME('2007-12-31 23:59:59.999999', '1 1:1:1.000002');



-- TIMEDIFF(expr1, expr2): expr1 - expr2
SELECT TIMEDIFF('2000-01-01 00:00:00', '2000-01-01 00:00:00.000001');
SELECT TIMEDIFF('2008-12-31 23:59:59.000001', '2008-12-30 01:01:01.000002');

-- DAYOFWEEK
SELECT 
'2023-04-30', -- SUN
DAYOFWEEK('2023-04-30'),
DATE_ADD('2023-04-30', INTERVAL 1 DAY),
DAYOFWEEK(DATE_ADD('2023-04-30', INTERVAL 1 DAY)),
DATE_ADD('2023-04-30', INTERVAL 6 DAY),
DAYOFWEEK(DATE_ADD('2023-04-30', INTERVAL 6 DAY));
